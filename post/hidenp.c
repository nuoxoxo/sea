#include <unistd.h>

int     main(int argc, char **argv)
{
        int     i;
        int     j;
        if (argc != 3)
        {
                write(1, "\n", 1);
                return (-1);
        }
        i = 0;
        j = 0;
        while (argv[1][i])
        {
            while (argv[2][j])
            {
                if (argv[2][j] == argv[1][i])   break;
                j++;
            }
            if (!argv[2][j] && argv[1][i])
            {
                write(1, "0", 1);
                return 0;
            }
            i++;
        }
        write(1, "1", 1);
        return 1;
}


