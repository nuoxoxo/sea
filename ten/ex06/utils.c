#include <unistd.h>

long    ft_add(int a, int b)
{
        return (a + b);
}

long    ft_sub(int a, int b)
{
        return (a - b);
}

long    ft_div(int a, int b)
{
        return (a / b);
}

long    ft_mult(int a, int b)
{
        return (a * b);
}

long    ft_mod(int a, int b)
{
        return (a % b);
}
